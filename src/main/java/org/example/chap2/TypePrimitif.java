package org.example.chap2;

/**
 * byte :
 * Taille : 8 bits
 * Plage de valeurs : -128 à 127    <br><br>
 *
 *
 * short :
 * Taille : 16 bits<br>
 * Plage de valeurs : -32,768 à 32,767  <br>
 *
 *
 * char :
 * Taille : 16 bits <br>
 * Plage de valeurs : 0 à 65,535 (représente un caractère Unicode)  <br><br>
 *
 *
 * int :
 * Taille : 32 bits<br>
 * Plage de valeurs : -2^31 à 2^31 - 1 (soit -2,147,483,648 à 2,147,483,647)    <br><br>
 *
 *
 * float :
 * Taille : 32 bits <br>
 * Plage de valeurs : Environ ±3.40282347E+38F (valeurs approximatives, avec une précision de 6 à 7 chiffres décimaux)  <br><br>
 *
 * long :
 * Taille : 64 bits <br>
 * Plage de valeurs : -2^63 à 2^63 - 1 (soit -9,223,372,036,854,775,808 à 9,223,372,036,854,775,807)    <br><br>
 *
 *
 * double :
 * Taille : 64 bits <br>
 * Plage de valeurs : Environ ±1.79769313486231570E+308 (valeurs approximatives, avec une précision de 15 à 16 chiffres décimaux)   <br><br>
 *
 * boolean :
 * Taille : non spécifiée (dépend de l'implémentation)  <br>
 * Plage de valeurs : true ou false
 */
